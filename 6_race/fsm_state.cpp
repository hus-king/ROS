/* 有限状态机（FSM）完整状态说明 */
switch (fsm_state) {
    /*****************************​ 初始化阶段 ​*****************************/
        case 0:  // 准备进入OFFBOARD模式
            // 目标：切换飞行模式到OFFBOARD（外部控制模式）
            // - 如果已进入OFFBOARD：转状态1（准备解锁）
            // - 否则每隔1秒尝试发送模式设置请求
            break;
    
        case 1:  // 等待解锁（ARM）
            // 目标：完成飞行器解锁
            // - 如果已解锁：初始化起飞位置并转状态2（起飞）
            // - 否则每隔1秒发送解锁指令
            break;
    
    /*****************************​ 起飞阶段 ​*******************************/
        case 2:  // 起飞过程
            // 目标：爬升到指定高度（fly_height）
            // - 使用PID控制垂直速度
            // - 达到目标高度后转状态3（导航）
            break;
    
    /*****************************​ 导航阶段 ​*******************************/
        case 3:  // 航点导航
            // 目标：按顺序访问预设航点
            // - 如果所有航点完成：转状态7（悬停）
            // - 到达当前航点时：
            //   - 悬停并转状态31（转向对准目标框）
            //   - 设置finding_state=2（进入目标搜索模式）
            break;
    
        case 31: // 转向对准目标
            // 目标：调整航向对准目标框
            // - 使用PID控制偏航角
            // - 达到目标角度后转状态5（目标搜索）
            break;
    
    /*****************************​ 目标操作阶段 ​***************************/
        case 5:  // 目标搜索模式
            // 目标：通过视觉识别定位目标框
            // - 超时未找到目标时：
            //   - finding_state=1：回退到导航（状态3）
            //   - finding_state=2：转状态63（后退观察）
            //   - finding_state=3：转状态64（方形搜索）
            // - 发现目标时：
            //   - 计算穿越路径并转状态61/62
            break;
    
        case 61: // 视觉对准微调
            // 目标：精确对准目标框中心
            // - 根据视觉反馈调整位置和角度
            // - 达到精度要求后转状态62（穿越执行）
            break;
    
        case 62: // 穿越执行
            // 目标：穿过已识别的目标框
            // - 沿预定轨迹移动
            // - 完成后更新航点索引并转回导航（状态3）
            break;
    
        case 63: // 后退观察模式
            // 目标：通过后退获取更好的观察视角
            // - 后退固定距离后返回搜索（状态5）
            break;
    
        case 64: // 方形搜索模式
            // 目标：在三维空间执行系统搜索
            // - 按预定方形轨迹移动
            // - 包含上升、平移、下降等复合动作
            break;
    
    /*****************************​ 异常处理阶段 ​***************************/
        case 9:  // 避障导航
            // 目标：绕过障碍物到达安全航点
            // - 使用修改后的导航逻辑
            // - 到达后转状态31（重新对准）
            break;
    
    /*****************************​ 任务结束阶段 ​***************************/
        case 7:  // 悬停待命
            // 目标：保持当前位置
            // - 5秒后转状态8（着陆）
            break;
    
        case 8:  // 着陆过程
            // 目标：安全降落
            // - 下降到指定高度后切换LAND模式
            // - 确认着陆后转结束状态（-1）
            break;
    
    /*****************************​ 特殊状态 ​*******************************/
        case -1: // 任务终止
            // 最终状态：所有操作终止
            break;
    
        default: // 异常状态处理
            break;
    }
    
    /* 状态转换特征说明
       - 层次化设计：初始化 → 起飞 → 导航 → 目标操作 → 降落
       - 模式切换：通过finding_state细分搜索策略
       - 安全设计：超时机制/避障/异常回退
       - 定位方式：结合视觉反馈（find_target）和PID控制
       - 导航逻辑：航点队列 + 动态路径修正 */